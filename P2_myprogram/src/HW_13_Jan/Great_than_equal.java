/* 1) write a program to implement the following: if age is greater than equal to 18,
 * print "Eligible to vote" or print "Not Eligible to vote"
*/

package HW_13_Jan;

public class Great_than_equal {
	public static void main(String[] arg) {
		int a = 25;
		if (a>18)
		{
			System.out.println("Eligible to vote age: "+a);
		}
		else
		{
			System.out.println("Not Eligible to vote age: "+ a);
		}
		
	}

}
